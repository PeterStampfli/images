/**
 * representing image pixel data, loading the image and doing something
 * @example see aaa.html
 *
 * @constructor InputImage
 */


function InputImage() {
    this.image = null;
    this.width = 0;
    this.limitX = 0;
    this.periodX = 0;
    this.height = 0;
    this.limitY = 0;
    this.periodY = 0;
    this.pixels = null;
    this.periodic = false;
}
